public class Student {
    private String masv;
    private String tensv;
    private String gt;
    private String ns;
    private String st;
    private String tt;
    private String nt;

    public Student(String mssv, String tensv, String gt, String ns, String st, String tt, String nt) {
        this.masv = mssv;
        this.tensv = tensv;
        this.gt = gt;
        this.ns = ns;
        this.st = st;
        this.tt = tt;
        this.nt = nt;
    }

    

    

    public String getMssv() {
        return masv;
    }

    public void setMssv(String mssv) {
        this.masv = mssv;
    }

    public String getTensv() {
        return tensv;
    }

    public void setTensv(String tensv) {
        this.tensv = tensv;
    }

    public String getGt() {
        return gt;
    }

    public void setGt(String gt) {
        this.gt = gt;
    }

    public String getNs() {
        return ns;
    }

    public void setNs(String ns) {
        this.ns = ns;
    }

    public String getSt() {
        return st;
    }

    public void setSt(String st) {
        this.st = st;
    }

    public String getTt() {
        return tt;
    }

    public void setTt(String tt) {
        this.tt = tt;
    }

    public String getNt() {
        return nt;
    }

    public void setNt(String nt) {
        this.nt = nt;
    }
    
    
}
